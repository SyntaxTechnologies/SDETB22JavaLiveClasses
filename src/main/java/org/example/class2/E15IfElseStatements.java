package org.example.class2;

public class E15IfElseStatements {
    public static void main(String[] args) {


      String name="James";
      // > < >= <= != == these should only be used with primitive data types
      if(!name.equals("John")){
          System.out.println("Hi John");
      }else{
          System.out.println("Hi Buddy");
      }


    }
}
